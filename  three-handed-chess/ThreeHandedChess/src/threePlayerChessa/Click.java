package threePlayerChessa;

public class Click {
	int num;
	int let;
	
	public Click(int num, int let) {
		super();
		this.num = num;
		this.let = let;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getLet() {
		return let;
	}
	public void setLet(int let) {
		this.let = let;
	}	
}
